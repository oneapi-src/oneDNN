void jit_avx_kernel_b0_sgemm_kern::generate() {
    Xbyak::Label l259c;
    Xbyak::Label l2774;
    Xbyak::Label l2834;
    Xbyak::Label l2cf4;

    preamble();
    auto stacksize = get_size_of_abi_save_regs();
#ifdef _WIN32
    mov(A, ptr[OLD_A]);
    mov(B, ptr[OLD_B]);
#endif
    mov(C, ptr[OLD_C]);
    mov(LDC, ptr[OLD_LDC]);

    mov(M, qword[M]);
    mov(N, qword[N]);
    mov(K, qword[K]);

    generate_part1(l2cf4, l2834, l2774, l259c);
    generate_part2(l2cf4, l2834, l2774, l259c);

    postamble();
}

